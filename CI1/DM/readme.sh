echo "num.sh applique une opération à chaque nombre d'une liste de nombres entiers positifs donnés en argument.

./num.sh [OP] [ARGS]...

OP est l'opération à appliquer, à choisir parmi:

* l ou len (pour longueur) : afficher le nombre de chiffres de chaque nombre
* m ou mir (pour miroir) : écrire chaque nombre à l'envers (en miroir)
* s ou sum (pour somme) : afficher la somme des chiffres de chaque nombre
* b ou bin (pour binaire) : écrire chaque nombre, donné sous forme décimale, en binaire
* d ou dec (pour décimal) : écrire chaque nombre, donné sous forme binaire, en décimal
* i ou int (pour interactif) : commencer la saisie interactive de l'opération à appliquer aux nombres

ARGS est la liste des nombres entiers positifs à traiter."